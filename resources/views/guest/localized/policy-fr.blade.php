# Politique de Confidentialité  
**Dernière mise à jour :** 22 avril 2025

Chez Omra Plus, nous accordons une importance primordiale à la confidentialité des utilisateurs et à la sécurité de leurs informations personnelles. Cette politique vise à expliquer comment nous traitons vos données personnelles collectées lors de l’utilisation de nos services, que ce soit via notre application mobile ou notre site internet.

---

## 2. Informations que nous collectons

Lorsque vous utilisez l’application ou le site web Omra Plus, nous collectons certaines informations que vous nous fournissez directement via des formulaires ou lors du processus de réservation. Ces informations sont essentielles pour offrir nos services de manière précise et sécurisée.

### 1. Nom complet  
Nous collectons votre nom complet tel qu’indiqué dans votre passeport ou pièce d’identité officielle, afin de :

- Finaliser les réservations et vérifier votre identité  
- Préparer les documents de voyage nécessaires (ex. : visa)  
- Vérifier vos données auprès des autorités officielles et des partenaires de service  

### 2. Coordonnées (numéro de téléphone, e-mail)  
Nous utilisons vos coordonnées pour :

- Envoyer des notifications instantanées concernant l’état de votre réservation  
- Vous informer des mises à jour ou modifications relatives à votre voyage  
- Fournir un support technique rapide en cas de besoin  
- Envoyer des offres promotionnelles ou du contenu marketing (avec votre consentement préalable)  

### 3. Détails du voyage (dates, hébergement)  
Ces informations nous permettent de :

- Personnaliser la réservation selon vos préférences  
- Organiser l’hébergement et le transport en fonction de votre emploi du temps  
- Envoyer des alertes concernant les départs et arrivées  
- Améliorer l’expérience utilisateur avec des recommandations personnalisées  

### 4. Informations de paiement et de facturation  
Nous collectons ces données pour :

- Traiter les paiements et vérifier la réussite de la transaction  
- Émettre des factures électroniques officielles  
- Régler les réservations avec les fournisseurs de services  
- Vous protéger contre les fraudes via des systèmes de vérification sécurisés  

---

## 3. Comment utilisons-nous vos informations ?

Les informations que vous nous fournissez sont utilisées manuellement pour vous offrir un service sécurisé, efficace et personnalisé. Cela inclut :

- **Exécution des réservations :** Utilisation de vos données pour compléter les réservations auprès des prestataires (transport, hébergement, etc.).  
- **Communication directe :** Envoi de notifications par SMS ou e-mail concernant votre situation ou pour répondre à vos questions.  
- **Conformité réglementaire :** Utilisation de certaines données pour répondre aux exigences du Ministère du Hajj ou d’autres autorités officielles en Arabie Saoudite.  

> Nous n’utilisons jamais vos données à des fins non déclarées et nous vous demanderons votre consentement explicite pour toute utilisation supplémentaire.

---

## 4. Qui peut accéder à vos informations ?

Nous partageons vos données uniquement si nécessaire et avec des tiers de confiance :

- **Fournisseurs de services :** Hôtels, compagnies aériennes, transporteurs terrestres ayant besoin de vos informations pour confirmer les réservations.  
- **Autorités gouvernementales :** Telles que le Ministère du Hajj ou la Direction Générale des Passeports, sur demande officielle ou obligation légale.  
- **Équipe de support interne :** Employés autorisés à résoudre les problèmes techniques ou opérationnels.  
- **Passerelles de paiement :** Pour traiter les paiements en toute sécurité et confirmer les transactions.  

> Tous nos partenaires sont contractuellement tenus de protéger vos données et ne peuvent les utiliser en dehors du cadre du service convenu.

---

## 5. Protection des données

Chez Omra Plus, nous prenons la sécurité des données très au sérieux et nous assurons leur protection à l’aide des mesures suivantes :

- **Chiffrement des données :** Vos données sont chiffrées lors du transfert à l’aide de protocoles tels que HTTPS/SSL.  
- **Contrôle d’accès :** Seuls les employés ou partenaires autorisés peuvent accéder à vos données.  
- **Audits réguliers :** Nous inspectons nos systèmes en continu pour détecter d’éventuelles failles de sécurité ou tentatives d’intrusion.  
- **Stockage sécurisé :** Les données sont hébergées sur des serveurs conformes aux normes internationales de protection des données.  

---

## 6. Durée de conservation des données

Nous conservons vos données personnelles uniquement pendant la durée nécessaire à la prestation de nos services ou conformément aux lois applicables. Une fois cette durée expirée, nous procédons à :

- La suppression ou la destruction sécurisée de vos données  
- Ou l’anonymisation des données à des fins d’analyse, sans possibilité de vous identifier  

> Vous pouvez demander à tout moment la suppression de votre compte et de vos données. Nous y répondrons dans un délai maximal de 15 jours ouvrables, sauf obligation légale contraire.

---

## 7. Droits de l’utilisateur

Nous respectons pleinement vos droits en matière de données personnelles. Vous avez le droit de :

- **Accéder à vos données :** Obtenir une copie des informations que nous détenons à votre sujet  
- **Mettre à jour ou corriger vos données :** Modifier toute information incorrecte  
- **Demander la suppression :** Supprimer votre compte et l’ensemble de vos données  
- **Refuser les messages promotionnels :** Choisir de ne plus recevoir de contenu marketing  

> Pour exercer ces droits, veuillez nous contacter via les coordonnées officielles ci-dessous.

---

## 8. Modifications de la politique de confidentialité

Nous nous réservons le droit de modifier ou de mettre à jour cette politique à tout moment, conformément aux évolutions légales ou opérationnelles. En cas de modification importante, nous :

- Mettrons à jour la date de révision en haut de la page  
- Considérerons votre utilisation continue de l’application comme une acceptation implicite de la nouvelle politique  

