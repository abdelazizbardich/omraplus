# Privacy Policy  
**Last Updated:** April 22, 2025

At Omra Plus, we place the utmost importance on user privacy and the security of their personal information. This policy aims to explain how we handle your personal data collected while using our services, whether through our mobile app or website.

---

## 2. Information We Collect

When you use the Omra Plus app or website, we collect some information that you provide directly through forms or during the booking process. This information is essential to delivering our services accurately and securely.

### 1. Full Name  
We collect your full name as shown in your passport or official ID, for the purposes of:

- Completing bookings and verifying your identity  
- Preparing required travel documents (e.g., visa)  
- Matching your data with official authorities and service partners  

### 2. Contact Information (Phone number, Email)  
We use your contact information to:

- Send real-time notifications about your booking status  
- Provide important updates or changes regarding your trip  
- Offer fast technical support when needed  
- Send promotional offers or marketing content (with your prior consent)  

### 3. Travel Details (Trip dates, Accommodation)  
This information helps us:

- Personalize bookings based on your preferred timing  
- Arrange accommodation and transportation according to your schedule  
- Send alerts regarding departure and arrival  
- Enhance the booking experience with personalized recommendations  

### 4. Payment and Billing Information  
We collect payment information for:

- Processing transactions and verifying successful payments  
- Issuing official electronic invoices  
- Settling bookings with service providers  
- Protecting you against fraud through secure verification systems  

---

## 3. How We Use Your Information

We manually use the information you provide to offer a secure, efficient, and personalized service. Our uses include:

- **Booking Execution:** Using your data to complete reservations with parties like transport or accommodation providers.  
- **Direct Communication:** Sending text or email notifications about your status, or responding to your inquiries.  
- **Regulatory Compliance:** Using some data to comply with the requirements of the Ministry of Hajj or other official authorities in Saudi Arabia.  

> We do not use your data for any undisclosed purposes and will request your explicit consent if additional use is required.

---

## 4. Who Has Access to Your Information?

We follow a data minimization principle and only share your information when necessary and with trusted parties:

- **Service Providers:** Such as hotels, airlines, or ground transport companies who need your information to confirm bookings.  
- **Government Authorities:** Such as the Ministry of Hajj and Umrah or Passport Department, when officially requested or legally required.  
- **Internal Support Team:** Authorized staff for resolving your technical or operational issues.  
- **Payment Gateways:** For securely processing payments and confirming transactions.  

> All partners are bound to protect your data and are not allowed to use it beyond the agreed-upon services.

---

## 5. Data Protection

At Omra Plus, we take data security seriously and work to protect your information through the following means:

- **Data Encryption:** Your data is encrypted during transmission using protocols like HTTPS/SSL.  
- **Access Control:** Only authorized employees or partners can access your information.  
- **Regular Audits:** We constantly review our systems to detect any vulnerabilities or potential breaches.  
- **Secure Storage:** Data is stored on secure servers that comply with international data protection standards.  

---

## 6. Data Retention Period

We retain your personal information only as long as necessary to provide services or as required by laws and regulations. Once no longer needed, we will:

- Safely delete or destroy the data  
- Or anonymize it for analytical purposes without linking it back to you  

> You can request deletion of your account and all associated data at any time. We will comply within 15 business days, unless legal obligations prevent it.

---

## 7. User Rights

We respect your full rights concerning your personal information. You have the right to:

- **Access Your Data:** Request a copy of all the information we hold about you.  
- **Update or Correct Data:** Modify or correct any inaccurate information.  
- **Request Deletion:** Permanently delete your account and all associated data.  
- **Opt-Out of Promotional Messages:** Choose not to receive any marketing content from us.  

> To exercise any of these rights, please contact us using the official channels listed below.

